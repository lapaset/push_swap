#include "libft.h"

void	ft_putnbr_ll(long long n)
{
	if (n < 0)
	{
		ft_putchar('-');
		n *= -1;
	}
	if (n < 10)
		ft_putchar(n + '0');
	else
	{
		ft_putnbr_ll(n / 10);
		ft_putchar(n % 10 + '0');
	}
}
