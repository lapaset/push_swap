int		ft_sqrt(int nb)
{
	int sqr;

	if (nb < 0)
		return (-1);
	if (nb == 0)
		return (0);
	sqr = 1;
	while (sqr * sqr <= nb)
	{
		if (sqr * sqr == nb)
			return (sqr);
		sqr++;
	}
	return (-1);
}
